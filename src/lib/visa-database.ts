// ─── Lookup Helpers ───────────────────────────────────────────────────────────

/** Find a country by ISO code */
export function getCountryByCode(code: string): CountryVisaProfile | undefined {
  return VISA_DATABASE.find((c) => c.countryCode === code);
}

/** Find a visa type within a country */
export function getVisaType(countryCode: string, visaTypeId: string): VisaType | undefined {
  return getCountryByCode(countryCode)?.visaTypes.find((v) => v.id === visaTypeId);
}

/** Get all countries as summaries (for country selector grid) */
export function getAllCountrySummaries() {
  return VISA_DATABASE.map((c) => ({
    countryCode: c.countryCode,
    countryName: c.countryName,
    flag: c.flag,
    continent: c.continent,
    capital: c.capital,
    currency: c.currency,
    visaFreeForEgyptian: c.visaFreeForEgyptian ?? false,
    embassyUrl: c.embassyUrl,
    visaTypeCount: c.visaTypes.length,
    visaTypeIds: c.visaTypes.map((v) => v.id),
    visaTypeLabels: c.visaTypes.map((v) => v.label),
    eVisa: c.visaTypes.some((v) => v.eVisa),
    visaOnArrival: c.visaTypes.some((v) => v.visaOnArrival),
    processingTime: c.visaTypes[0]?.processingTime ?? "Varies",
  }));
}
