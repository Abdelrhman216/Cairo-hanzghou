// ─── Helper: Build document array from ids ────────────────────────────────────

function docs(ids: string[], overrides: Partial<VisaDocument>[] = []): VisaDocument[] {
  return ids.map((id, i) => ({
    ...DOCUMENT_REGISTRY[id],
    ...(overrides[i] ?? {}),
  }));
}

